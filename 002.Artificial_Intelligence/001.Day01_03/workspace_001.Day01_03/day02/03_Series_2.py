'''
Series示例
访问 Series（新版 pandas 标准写法）
'''

import pandas as pd


# ===============================
# 默认整数索引
# ===============================

s01 = pd.Series([100, 90, 80, 70])
print(s01.iloc[0])   # 使用 iloc 按位置取值
# print(s01.iloc[-1])  # 位置索引支持负数

print("##" * 20)

# ===============================
# 自定义字符串索引
# ===============================

s01 = pd.Series([100, 90, 80, 70], index=['zs', 'ls', 'ww', 'zl'])

# 按位置索引（必须使用 iloc）
print(s01.iloc[0])

print("##" * 20)

# 按位置取最后一个
print(s01.iloc[-1])


# ===============================
# 位置索引（推荐使用 iloc）
# ===============================

print("##" * 20)
print(s01.iloc[0])  # 位置索引

print("##" * 20)
print(s01.iloc[:2])  # 切片（前两个），不包括第2个

print("##" * 20)
print(s01.iloc[[0, 1, 3]])  # 位置掩码


# ===============================
# 标签索引（使用 loc）
# ===============================

print("##" * 20)
print(s01.loc['zs'])  # 标签索引

print("##" * 20)
print(s01.loc[:'ls'])  # 标签切片（包含结束位置）

print("##" * 20)
print(s01.loc[['zs', 'ls', 'zl']])  # 标签掩码